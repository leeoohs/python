# 想出至少三总动物，使用for循环将每种动物名称显示出来
animals = ['monkey', 'panda', 'bee']
for animal in animals:
    print(animal)

# 针对每个动物都打印一个句子，如"A dog would make a great pet"
animals = ['monkey', 'panda', 'bee']
for animal in animals:
    print(animal)
    print("A " + animal + " would make a great pet." + "\n")

# 在程序末尾添加一行代码，指出动物的共同之处，如："Any of these animals would make a great pet!"
animals = ['monkey', 'panda', 'bee']
for animal in animals:
    print(animal)
    print("A " + animal + " would make a great pet." + "\n")

print("Any of these animals would make a great pet!")